>>Inner Join
Fetch the matched values from both the tables

>>Left Join
Inner Join plus the values remaining in the left side of the table

>>Right Join
Inner Join plus the values remaining in the Right side of the table

>>Full Join
Left Join Values + Right Join Values

>>Self Join
Joins a table to itself. Useful for comparing rows within the same table.
Rows Compared Within the Same Table

>>Cross Join
Returns the Cartesian product of two tables, meaning every row in the first table is combined with every row in the second table.
All combinations of rows




